
public class No400To500 {

	public static void main(String[] args) {
		int i=400;
		for(i=400;i<=500;i+=7)
		{
			System.out.println(i);
		
		}
 System.out.println("USING DO WHILE LOOP");
 	i=400;	
 	do
 		{
 			System.out.println(i);
 			i+=7;
 		}while(i<=500);
 	
 	System.out.println("USING WHILE LOOP");
 	i=400;
 	while(i<=500)
 	{
			System.out.println(i);
			i+=7;
 	}

	}

}
